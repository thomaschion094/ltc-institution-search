def get_city_name(city_code):
    """根據縣市代碼取得縣市名稱"""
    return CITY_DISTRICT_MAPPING.get(city_code, {}).get("name", "未知縣市")

def get_district_name(city_code, district_code):
    """根據縣市代碼和區域代碼取得區域名稱"""
    city_data = CITY_DISTRICT_MAPPING.get(city_code, {})
    districts = city_data.get("districts", {})
    return districts.get(district_code, "未知區域")

def get_all_cities():
    """取得所有縣市列表"""
    return [{"code": code, "name": data["name"]} 
            for code, data in CITY_DISTRICT_MAPPING.items()]

def get_districts_by_city(city_code):
    """根據縣市代碼取得所有區域列表"""
    city_data = CITY_DISTRICT_MAPPING.get(city_code, {})
    districts = city_data.get("districts", {})
    return [{"code": code, "name": name} 
            for code, name in districts.items()]

